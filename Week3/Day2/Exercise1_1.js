fruits.shift(); //removes "Banana" from fruits
fruits.sort(); //it's sorting our fruits//-----------------------------------------------------------//
fruits.push("Kiwi"); //Adds "Kiwi" to fruits
fruits.splice(0, 1); //Remove "Apple" from fruits//-----------------------------------------------------------//
fruits.reverse(); //Reverse our fruits
